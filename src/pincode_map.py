def get_area_name(area_str):
    """Convert '560036 (India)' to 'Indiranagar' or '560036' to 'Indiranagar'."""
    if not area_str:
        return "Unknown"
    pin = area_str.replace(" (India)", "").replace("(India)", "").strip()
    return PINCODE_AREAS.get(pin, area_str)
